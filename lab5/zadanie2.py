print(f"a) √81 = {wynik_a}")


wynik_b = 810
print(f"b) 810 = {wynik_b}")


wynik_c = math.sqrt(2) + math.sqrt(3) + math.sqrt(6)
print(f"c) √2 + √3 + √6 = {wynik_c:.3f}")


wynik_d = cmath.sqrt(-5)  
print(f"d) √−5 = {wynik_d}")


wynik_e = 3 * math.sqrt(125)
